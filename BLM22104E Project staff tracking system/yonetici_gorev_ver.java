    private void btn_workActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_workActionPerformed
        
        Calisan np1 = new Calisan();
        
        np1.gorev = cmbx_work.getSelectedItem().toString();
        np1.eposta = txt_mail.getText();
        
        
        if(txt_mail.getText().isBlank())
            JOptionPane.showMessageDialog(rootPane, "gorev vermek için tablodan çalışan seçin");
        
        
        else if (SirketDB.gorevDuzenle(np1)) {

            JOptionPane.showMessageDialog(rootPane, "gorev verildi");
            np1.durum = "verildi";

            tabloyaBilgiYazdir();
            btn_refreshActionPerformed(evt);

        } else {
            JOptionPane.showMessageDialog(rootPane, "sorun oluştu, lütfen tekrar deneyin");
        }
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btn_workActionPerformed

