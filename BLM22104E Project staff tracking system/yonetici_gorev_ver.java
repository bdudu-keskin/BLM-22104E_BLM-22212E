    private void tbl_gorevMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_gorevMouseClicked
        // TODO add your handling code here:

        int r = tbl_gorev.getSelectedRow();

        if(r == -1)
        return;

        String name = (String) tbl_gorev.getValueAt(r, 0);
        txt_name.setText(name);

        String surname = (String) tbl_gorev.getValueAt(r, 1);
        txt_surname.setText(surname);
        
        String email = (String) tbl_gorev.getValueAt(r, 2);
        txt_mail.setText(email);

        String gorev = (String) tbl_gorev.getValueAt(r, 3);
        cmbx_work.setSelectedItem(gorev);

    }//GEN-LAST:event_tbl_gorevMouseClicked

    
    
    
    
    
    
    
    
