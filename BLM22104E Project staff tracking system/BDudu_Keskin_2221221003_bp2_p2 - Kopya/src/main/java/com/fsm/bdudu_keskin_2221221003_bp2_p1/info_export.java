    private void tabloyaBilgiYazdir() {

        ArrayList<Calisan> calisanlar = SirketDB.calisanGorevYazdir();
        tbl_adminGorev.setRowCount(0);
        for (Calisan calisan : calisanlar) {
            tbl_adminGorev.addRow(new Object[]{calisan.isim, calisan.soyisim, calisan.eposta, calisan.gorev, calisan.durum});

        }

    }

    
  

