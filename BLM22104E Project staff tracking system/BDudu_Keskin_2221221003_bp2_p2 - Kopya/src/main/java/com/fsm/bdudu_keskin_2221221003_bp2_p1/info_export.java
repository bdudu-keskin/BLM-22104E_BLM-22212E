    private void btn_exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_exportActionPerformed
        // TODO add your handling code here:

        int r = tbl_gorev.getSelectedRow();
        if (r == -1)
            JOptionPane.showMessageDialog(rootPane, "bilgi yazdırmak için satır seçiniz");

        else if (r >= 0) {

            if (txt_dosyaAdi.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "dosya adi giriniz");
                
            } else {
                SirketDB.dosyayaYazdir(tbl_gorev, txt_dosyaAdi.getText());
            }

        }
    }//GEN-LAST:event_btn_exportActionPerformed

